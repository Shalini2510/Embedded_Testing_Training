# Creating a Python list
list1 = [10, 20, 30, 40, 50]

#list slicing
print("Original list:", list1)
print("Slicing first three elements:", list1[:3])  # Elements from index 0 to 2
print("Slicing last two elements:", list1[-2:])   # Elements from index -2 to the end
print("Slicing from second to fourth element:", list1[1:4])  # Elements from index 1 to 3

# Appending new elements to the list
list1.append(60)
print("List after appending 60:", list1)
list1.append(70)
print("List after appending 70:", list1)
